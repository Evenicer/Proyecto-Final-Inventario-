             Class.forName("java.sql.Driver");
             Connection con =(Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/inventariodb","root","");
             stmt = (Statement) con.createStatement();
             String query = "update inventario set nombre='"+txtNombre.getText()+"' , precio='"+txtPrecio.getText()+"' , categoria='"+comboCat.getSelectedItem().toString()+"' where id='"+txtId.getText()+"' ";
