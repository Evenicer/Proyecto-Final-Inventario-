    private void btnConsularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsularActionPerformed
        try{
            Class.forName("java.sql.Driver");
            Connection con =(Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/inventariodb","root","");
        
            String query = "select * from inventario where id='"+txtId.getText()+"'";
            PreparedStatement statement = con.prepareStatement(query);
            
            ResultSet set = statement.executeQuery();
            
            while(set.next()){
                txtNombre.setText(set.getString("Nombre"));
                txtPrecio.setText(set.getString("Precio"));
                comboCat.setSelectedItem(set.getString("Categoria"));
            
            
            }
        
        
        }catch(Exception ex){
        JOptionPane.showMessageDialog(null , "ERROR " +ex); 
        
        }
    }//GEN-LAST:event_btnConsularActionPerformed

